package com.example.james.healthybaby;

public class PointValue {
    double xValue, yValue;

    public PointValue() {
    }

    public PointValue(double xValue, double yValue) {
        this.xValue = xValue;
        this.yValue = yValue;
    }

    public double getxValue() {
        return xValue;
    }

    public double getyValue() {
        return yValue;
    }
}
